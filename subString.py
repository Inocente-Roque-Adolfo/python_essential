# uso de substring en python - cadena[comienzo:final]

#inicio de la cadena
cadena = "Hola Mundo"
print(cadena.upper().center(20, '*')) # ******Hola Mundo*****
print('',cadena[0]) # H
print('',cadena[-1]) # o final de la cadena

print(cadena[0:4]) # Hola

#final de la cadena'
cadena = "Hola Mundo"
print(cadena[5:10]) # Mundo

#saltos en la cadena
cadena = "Hola Mundo"
print(cadena[0:10:2]) # Hl ud


