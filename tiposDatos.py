#datos enteros

entero = 10
print(entero, type(entero))
print("El tipo de dato de la variable entero es:", type(entero))


#datos flotantes
flotante = 10.5
print(flotante, type(flotante))
print("El tipo de dato de la variable flotante es:", type(flotante))

#datos complejos
complejo = 10 + 5j
print(complejo, type(complejo))
print("El tipo de dato de la variable complejo es:", type(complejo))

#datos string
cadena = 'hola mundo'
print(cadena, type(cadena))
print("El tipo de dato de la variable cadena es:", type(cadena))

#datos booleanos
booleano = 3==4
print(booleano, type(booleano))
print("El tipo de dato de la variable booleano es:", type(booleano))

#datos lista
lista = [1, 2, 3, 4, 5]
print(lista, type(lista))
print("El tipo de dato de la variable lista es:", type(lista))

#datos tupla
tupla = (1, 2, 3, 4, 5)
print(tupla, type(tupla))
print("El tipo de dato de la variable tupla es:", type(tupla))

#datos diccionario
diccionario = {'nombre': 'Juan', 'edad': 22}
print(diccionario, type(diccionario))
print("El tipo de dato de la variable diccionario es:", type(diccionario))

#datos conjunto set
conjunto = {1, 2, 3, 4, 5}
print(conjunto, type(conjunto))
print("El tipo de dato de la variable conjunto es:", type(conjunto))

#datos bytes
bytes = b'hola'
print(bytes, type(bytes))
print("El tipo de dato de la variable bytes es:", type(bytes))

#datos bytearray
bytearray = bytearray(5)
print(bytearray, type(bytearray))
print("El tipo de dato de la variable bytearray es:", type(bytearray))

#datos memoria
memoria = memoryview(bytes)
print(memoria, type(memoria))
print("El tipo de dato de la variable memoria es:", type(memoria))

#datos None
nulo = None
print(nulo, type(nulo))
print("El tipo de dato de la variable nulo es:", type(nulo))

#datos range
rango = range(5)
print(rango, type(rango))
print("El tipo de dato de la variable rango es:", type(rango))

#datos frozenset
frozenset = frozenset(conjunto)
print(frozenset, type(frozenset))
print("El tipo de dato de la variable frozenset es:", type(frozenset))

#datos bool
bool = bool(4)
print(bool, type(bool))
print("El tipo de dato de la variable bool es:", type(bool))

#datos int
entero = int(10)
print(entero, type(entero))

#datos float
flotante = float(10.5)
print(flotante, type(flotante))

#datos complex
complejo = complex(10, 5)
print(complejo, type(complejo))

#datos str
cadena = str('hola mundo')
print(cadena, type(cadena))

#datos list
lista = list([1, 2, 3, 4, 5])
print(lista, type(lista))

#datos tuple
tupla = tuple((1, 2, 3, 4, 5))
print(tupla, type(tupla))

#datos dict
diccionario = dict(nombre='Juan', edad=22)
print(diccionario, type(diccionario))

#datos set
conjunto = set({1, 2, 3, 4, 5})
print(conjunto, type(conjunto))

#datos memoryview
memoria = memoryview(bytes)
print(memoria, type(memoria))









